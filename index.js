'use strict';
(function(){
function sum(a,b) {
    let result = a + b;
    return result;
}

function mult(a,b) {
    let result = a * b;
    return result;
}

function min(a,b) {
    let result = a - b;
    return result;
}

console.log(sum(3,4));
console.log(mult(5,4));
console.log(min(6,4));
})();
